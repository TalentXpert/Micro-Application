    public class UIControlFactory : UIControlBaseFactory
    {
        IBaseLibraryServiceFactory SF;

        public IServiceFactory ServiceFactory { get; }

        public UIControlFactory(IServiceFactory ServiceFactory):base(ServiceFactory)
        { SF = ServiceFactory; this.ServiceFactory = ServiceFactory;}

        public override  UIControl GetUIControl(Guid? organizationId, AppControl appControl, AppFormControl appFormControl, List<string>? values, Guid? parentId,bool addEmptyEntry)
        {
            List<SmartControlOption> options = new List<SmartControlOption>();
            Guid formId;
            if (organizationId == null)
                organizationId = Guid.Empty;
            switch (appControl.ControlIdentifier)
            {
                case ApplicationControls.Country:
                    formId = Guid.Parse(ApplicationForm.CountryFormId);
                    options = SF.PageDataStoreService.GetFormData(null, organizationId.Value, formId, null).Select(d => new SmartControlOption(d.Name, d.Id.ToString())).ToList();
                    break;

                case ApplicationControls.State:
                    formId = Guid.Parse(ApplicationForm.StateFormId);
                    if (IsNotNullOrEmpty(parentId))
                    {
                        options = SF.PageDataStoreService.GetFormData(null, organizationId.Value, formId, parentId).Select(d => new SmartControlOption(d.Name, d.Id.ToString())).ToList();
                    }
                    break;

                case ApplicationControls.City:
                    formId = Guid.Parse(ApplicationForm.CityFormId);
                    if (IsNotNullOrEmpty(parentId))
                    {
                        options = SF.PageDataStoreService.GetFormData(null, organizationId.Value, formId, parentId).Select(d => new SmartControlOption(d.Name, d.Id.ToString())).ToList();
                    }
                    break;

                case ApplicationControls.AssetGroup:
                    formId = Guid.Parse(ApplicationForm.AssetGroupFormId);
                    options = SF.PageDataStoreService.GetFormData(null, organizationId.Value, formId, null).Select(d => new SmartControlOption(d.Name, d.Id.ToString())).ToList();
                    break;
                case ApplicationControls.AssetSubgroup:
                    formId = Guid.Parse(ApplicationForm.AssetSubgroupFormId);
                    if (IsNotNullOrEmpty(parentId))
                    {
                        options = SF.PageDataStoreService.GetFormData(null, organizationId.Value, formId, parentId).Select(d => new SmartControlOption(d.Name, d.Id.ToString())).ToList();
                    }
                    break;
                case ApplicationControls.AssetUnit:
                    options = GetOptions("No;Kg;Ltr;Mtr");
                    break;
                case ApplicationControls.ControlDataTypes:
                    options = ControlDataType.GetControlDataTypes().Select(d => new SmartControlOption(d.Name, d.Name)).ToList();
                    break;
                case ApplicationControls.ControlType:
                    options = ControlTypes.GetControlTypes().Select(d => new SmartControlOption(d.Name, d.Name)).ToList();
                    break;
                case ApplicationControls.PermissionSelection:
                    options = ServiceFactory.UserRoleService.GetRolePermissionOptions(parentId);
                    break;
            }

                
            return BuildUIControl(appControl, appFormControl,values, options, addEmptyEntry);
        }


        public override string? GetTextFromId(string controlIdentifier, Guid id)
        {
            switch (controlIdentifier)
            {
                case ApplicationControls.Country:
                case ApplicationControls.State:
                case ApplicationControls.City:
                case ApplicationControls.AssetGroup:
                case ApplicationControls.AssetSubgroup:
                    return SF.PageDataStoreService.GetFormDatum(id)?.Name;
                default:
                    throw new ValidationException($"UIControlFactory-GetTextValue logic not implemented for control with identifier {controlIdentifier}.");
            }
        }

        public override UIControl GetComplexUIControl(AppControl appControl, AppFormControl appFormControl, Guid dataKey)
        {
            throw new NotImplementedException();
        }
    }

